<h2 class="text-2xl font-bold mt-8 mb-2"><b>Image Rules</b></h2>
<p class="mb-2">
  You can provide images for guides, but there are strict rules:
</p>
<ul class="list-disc list-inside mb-4">
  <li><b>Do not steal images</b> from other guide pages.</li>
  <li>Do not use images that <b>can't be legally used</b> by everyone.</li>
  <li>
    If using images from sites (like Wikipedia),
    <b>provide the source link</b>; otherwise, we won't use it.
  </li>
  <li>Take screenshots from Google Maps yourself.</li>
  <li>
    Make maps with Google yourself (to display specific highways or other
    details).
  </li>
  <li>Remove metadata from images.</li>
</ul>

